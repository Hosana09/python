print("Vamos aprender a aritmética com números inteiros!")
a = int(input("Digite o número inteiro para ser atribuído à letra a: "))
b = int(input("Digite o número inteiro para ser atribuído à letra b: "))

print("a =", a)
print("b =", b)
print("a + b =", a + b)
print("a - b =", a - b)
print("a * b =", a * b)
print("a ** b =", a ** b)
print("a // b =", a // b)
print("a % b =", a % b)